def fix_file_encoding(file_path):
    """修复单个文件的编码错误"""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()

        original_content = content
        fixed_count = 0

        # 应用编码修复
        for wrong, correct in ENCODING_FIXES.items():
            if wrong in content:
                content = content.replace(wrong, correct)
                fixed_count += 1
                print(f"  修复: {wrong} -> {correct}")

        # 如果有修复，则写回文件
        if content != original_content:
            with open(file_path, 'w', encoding='utf-8') as f:
                f.write(content)
            print(f"✓ 修复完成: {file_path} (修复了 {fixed_count} 处)")
            return True
        else:
            return False

    except Exception as e:
        print(f"✗ 修复失败: {file_path} - {str(e)}")
        return False

def main():
    """主函数"""
    if len(sys.argv) != 2:
        print("用法: python fix-encoding-issues.py <Java文件或目录路径>")
        sys.exit(1)

    path = Path(sys.argv[1])

    if not path.exists():
        print(f"错误: 路径不存在: {path}")
        sys.exit(1)

    fixed_files = 0
    total_files = 0

    if path.is_file():
        if path.suffix == '.java':
            total_files = 1
            if fix_file_encoding(path):
                fixed_files = 1
        else:
            print("错误: 只能处理 .java 文件")
            sys.exit(1)
    else:
        # 遍历目录
        for java_file in path.rglob("*.java"):
            total_files += 1
            if fix_file_encoding(java_file):
                fixed_files += 1

    print(f"\n修复完成!")
    print(f"总文件数: {total_files}")
    print(f"已修复: {fixed_files}")
    print(f"无需修复: {total_files - fixed_files}")

if __name__ == "__main__":
    main()